    def __init__(self, id, name, brand, type, location, unit_of_measure, status, balance, min_stock, deficit):
        self.id = id
        self.name = name
        self.brand = brand
        self.type = type
        self.location = location
        self.unit_of_measure = unit_of_measure
        self.status = status
        self.balance = balance
        self.min_stock = min_stock
        self.deficit = deficit